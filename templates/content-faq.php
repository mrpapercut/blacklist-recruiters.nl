<h2>FAQ</h2>
<div id="faq" class="faq">
	<div class="faqitem">
		<h3 class="question">Waarom is blacklist-recruiters.nl opgericht?</h3>
		<div class="answer">Recruiters gebruiken steeds bizardere taktieken om in contact met ontwikkelaars te raken, ongeacht welke situatie de ontwikkelaar zich op dat moment in bevind. Van ongevraagd spammen met nieuwsbrieven zonder uitschrijfknop (wat overigens door de Telecommunicatiewet van 2009 strafbaar is), herhaaldelijk telefoneren en emailen, tot misleiding en het onjuist afhandig maken van persoonlijke gegevens. De praktijken gaan steeds verder en er dient wat aan gedaan te worden.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Wat is het doel van blacklist-recruiters.nl?</h3>
		<div class="answer">De praktijken van recruitmentbureaus aan het licht brengen, zodat iedereen kan zien op welke wijze deze personen omgaan met hun potentiele klanten. Door deze lijst aan te maken kunnen werkgevers van te voren inzien wat voor bureau ze mee in zee gaan.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Willen jullie alle recruiters aanpakken?</h3>
		<div class="answer">Nee. Er is een duidelijk verschil in aanpak tussen verschillende bureaus. Wij richten ons vooral op de bureaus die ongevraagd CVs spammen naar bedrijven zonder afspraak of ook maar open vacatures. Op de bureaus die vooral zo veel mogelijk "profielen" in hun database willen, die nieuwsbrieven vol dezelfde (spook)vacatures sturen. De bureaus die niet luisteren naar smeekbedes om te stoppen met benaderingen of data-verwijdering. En de bureaus die geen verschil weten tussen een Java developer en een Javascript developer.<br><br>Er is overduidelijk een plek voor goede recruiters die vakkundig op zoek gaan naar een passende vacature of een ideale kandidaat. Maar helaas is er bij veel bureaus een totaal gebrek aan professionaliteit en ethiek. En die willen wij aanpakken.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Waarom specifiek IT recruiters?</h3>
		<div class="answer">Daar heeft de beheerder de meeste ervaring mee. Ook lijkt het in andere bedrijfstakken minder (of op een minder grote schaal) voor te komen.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Waarom vermeldt deze site alleen bedrijven en niet individuele recruiters?</h3>
		<div class="answer">Individuele recruiters wisselen zelf ook van baan, in dezelfde of een andere bedrijfstak. Daarbij spreken zij namens hun werkgever en kan alleen die werkgever ook wat veranderen aan de werkwijzen.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Waarom hanteren recruiters dit soort praktijken?</h3>
		<div class="answer">Er valt verschrikkelijk veel geld te verdienen als recruitmentbureau. Dat gecombineerd met de IT  branche waar een overschot aan banen is, heeft de afgelopen jaren het aantal recruitmentbureaus exponentieel doen zien groeien. En als er zo veel vissers in een te kleine vijver vissen, wordt veelal gekozen voor een agressievere aanpak in plaats van een superieur kwalitatieve aanpak.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Hoeveel verdienen recruiters?</h3>
		<div class="answer">Dit verschilt erg per bedrijf. De meeste vergoedingen liggen rond 25%, met uitschieters tot 50%. Dus als een nieuwe werknemer 30,000 euro per jaar gaat verdienen, is de beloning voor het recruitmentbureau 15,000 euro. Dit lijkt een van de redenen waarom er zoveel recruiters bijgekomen zijn de laatste jaren.<br><br>
Overigens is deze prijs niet gelijk per bedrijf. Een site als Bonque, die meer als jobsite dan recruitmentbureau wordt gezien, kan een veel lagere vergoeding vragen. Dit zijn echter uitzonderingen.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Wat vinden werkgevers van recruiters?</h3>
		<div class="answer">Werkgevers worden bestookt met CVs en aanbiedingen van recruiters, ook ongevraagd. CVs die veelal alle persoonlijke informatie van kandidaten bevatten, worden dus lukraak naar willekeurige bedrijven gestuurd. Het Bel-me-niet register wordt volledig genegeerd, alsmede ook eisen dat er geen contact meer wordt opgenomen.<br><br>
Verder komt het ook vaak voor dat recruiters dezelfde kandidaten opnieuw ergens anders willen plaatsen, en dus na een paar maanden opnieuw de aangenomen werknemer contacteren met nieuwe aanbiedingen. Dus dan heeft de werkgever net een vergoeding van vele duizenden euros gedaan aan een recruitmentbureau, en vervolgens wordt geprobeerd de kersverse werknemer weer weg te kapen. Dit klinkt misschien onrealistisch, maar het gebeurt heel erg vaak.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Zijn er ook goede IT recruiters? Kan daar een white-list voor komen?</h3>
		<div class="answer">Jazeker, niet alle wervings- en selectiebureas hanteren deze agressieve methodes. Om suggesties van belangenverstrengelingen te ontlopen bieden we geen white-list met "goedgekeurde" bureaus. Maar het ontbreken van deze goede recruiters op onze site kan een indicatie zijn.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Het moet wel fijn zijn om zo gewild te zijn en zo veel aandacht te krijgen. En dat terwijl er zoveel mensen werkloos thuis zitten.</h3>
		<div class="answer">Het is een illusie te denken dat recruiters zo veel mogelijk mensen contacteren omdat ze zo graag mensen aan een baan helpen. Recruiters hechten erg weinig waarde aan de wensen en de huidige situatie van hun potentiele kandidaten. Het overgrote deel van de ontwikkelaars heeft een baan en is niet op zoek naar iets anders. Toch worden deze mensen benaderd en hun persoonlijke informatie opgeslagen, zodat recruitmentbureaus naar een potentiele klant kunnen met de bewering "wij hebben 500 mensen in onze database".<br><br>
Het aanbod van recruiters bestaat ook uit heel veel zogenaamde "spookvacatures": vacatures die niet bestaan, maar worden gebruikt om mensen binnen te lokken. Ook komt het vaak voor dat een mogelijke kandidaat hun eigen baan krijgt aangeboden, of een baan bij een bedrijf waar ze al gewerkt hebben.</div>
	</div>

	<div class="faqitem">
		<h3 class="question">Hoe betrouwbaar is de informatie op deze site?</h3>
		<div class="answer">Blacklist-recruiters.nl doet zoveel mogelijk hun best de inhoud van meldingen op waarheid te beoordelen alvorens deze te plaatsen. Dit op basis van persoonlijke ervaring, of met behulp van screenshots. Mocht bewezen worden dat er foutieve informatie op de site is terecht gekomen, kan contact worden opgenomen met de beheerder.</div>
	</div>

