	</div>

	<div class="faqitem">
		<h3 class="question">Hoe zit het met mijn privacy als ik een melding maak?</h3>
		<div class="answer">Blacklist-recruiters.nl slaat geen persoonlijke informatie op behalve de informatie die middels een melding wordt toegestuurd. Het is mogelijk om anoniem te melden, en deze meldingen hebben evenveel waarde als meldingen met naam. Meldingen per email worden geplaatst zoals gewenst, exclusief email-adres van de afzender.</div>
	</div>

